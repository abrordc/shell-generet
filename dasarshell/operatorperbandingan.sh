#!/bin/bash

nilai_a=11
nilai_b=4

# >

echo "apakah nilai a lebih besar dari nilai b: $((nilai_a > nilai_b)) "

# ==

echo "apakah nilai a sama dengan nilai b: $((nilai_a == nilai_b)) "

# < 

echo "apakah nilai a lebih kecil dari nilai b: $((nilai_a < nilai_b)) "

# ! = 

echo "apakah nilai a tidak sama dengan nilai b: $((! (nilai_a == nilai_b))) "